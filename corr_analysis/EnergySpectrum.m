% Построение графика нормированного энергетического
% спектра прямоугольного сигнала
hF = figure('Color', 'w');
%axes1(hF);
x = [-2.5*pi:0.01:2.5*pi];

%---------------------------------------------------
% Нормированная спектральная плотность
y = abs(sin(x)./x);
subplot(2,1,1)   % первые координатные оси
plot(x, y, 'k-', 'LineWidth', 2)
set(gca, ...
    'XGrid', 'on', ...
    'YGrid', 'on', ...
    'XTick', [-2*pi -3*pi/2 -pi -pi/2 0 pi/2 pi 3*pi/2 2*pi], ...
    'FontName', 'Symbol', ...
    'XTickLabel', '-2p|-3p/2|-p|-p/2|0|p/2|p|3p/2|2p')

%---------------------------------------------------
% Нормированный энергетический спектр
y1 = sin(x).^2./(x.^2);
subplot(2,1,2)   % вторые координатные оси
plot(x, y1, 'k--', 'LineWidth', 2)
set(gca, ...
    'XGrid', 'on', ...
    'YGrid', 'on', ...
    'XTick', [-2*pi -3*pi/2 -pi -pi/2 0 pi/2 pi 3*pi/2 2*pi], ...
    'FontName', 'Symbol', ...
    'XTickLabel', '-2p|-3p/2|-p|-p/2|0|p/2|p|3p/2|2p')
% axis([-3*pi/2 3*pi/2 0 1])
